export function getPointsForTrack(trackName: string): Point[] {
  const name = (trackName || '').toLowerCase();
  if (name.includes('monza') || name.includes('italy') || name.includes('italian')) {
    return monzaPoints;
  } else if (name.includes('silverstone') || name.includes('britain') || name.includes('british')) {
    return silverstonePoints;
  } else if (name.includes('spa') || name.includes('belgian') || name.includes('belgium') || name.includes('francorchamps')) {
    return spaPoints;
  } else if (name.includes('barcelona') || name.includes('spain') || name.includes('spanish') || name.includes('catalunya')) {
    return barcelonaPoints;
  } else if (name.includes('austria') || name.includes('austrian') || name.includes('red bull ring') || name.includes('spielberg')) {
    return austriaPoints;
  } else {
    return monacoPoints; // default fallback
  }
}

export function isDrsZone(trackName: string, progress: number): boolean {
  const name = (trackName || '').toLowerCase();
  if (name.includes('monza')) {
    return (progress >= 0.82 || progress <= 0.18) || (progress >= 0.50 && progress <= 0.65);
  } else if (name.includes('silverstone')) {
    return (progress >= 0.22 && progress <= 0.38) || (progress >= 0.70 && progress <= 0.88);
  } else if (name.includes('spa')) {
    return (progress >= 0.42 && progress <= 0.60) || (progress >= 0.90 || progress <= 0.12);
  } else if (name.includes('barcelona') || name.includes('spain') || name.includes('spanish') || name.includes('catalunya')) {
    return (progress >= 0.88 || progress <= 0.12) || (progress >= 0.40 && progress <= 0.55);
  } else if (name.includes('austria') || name.includes('austrian') || name.includes('red bull ring') || name.includes('spielberg')) {
    return (progress >= 0.85 || progress <= 0.10) || (progress >= 0.15 && progress <= 0.35) || (progress >= 0.38 && progress <= 0.52);
  } else {
    return (progress >= 0.85 || progress <= 0.15);
  }
}

export function getSectorForProgress(trackName: string, progress: number): number {
  const name = (trackName || '').toLowerCase();
  let s1End = 0.33;
  let s2End = 0.66;

  if (name.includes('monza')) {
    s1End = 0.31;
    s2End = 0.64;
  } else if (name.includes('silverstone')) {
    s1End = 0.30;
    s2End = 0.65;
  } else if (name.includes('spa')) {
    s1End = 0.28;
    s2End = 0.66;
  } else if (name.includes('barcelona') || name.includes('spain') || name.includes('spanish') || name.includes('catalunya')) {
    s1End = 0.30;
    s2End = 0.68;
  } else if (name.includes('austria') || name.includes('austrian') || name.includes('red bull ring') || name.includes('spielberg')) {
    s1End = 0.30;
    s2End = 0.65;
  } else if (name.includes('monaco')) {
    s1End = 0.32;
    s2End = 0.68;
  }

  if (progress <= s1End) return 1;
  if (progress <= s2End) return 2;
  return 3;
}

